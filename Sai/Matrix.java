
public class Matrix 
{

	public static void main(String[] args)
	{
		
		int[][] matrix= {{2, 3} , {7,8} };
		
		System.out.println("Matrix");
		
		for(int i=0; i<matrix.length;i++)
		{
			for(int j=0;j< matrix.length;j++)
			{
				
				
				
				System.out.print(matrix[i][j] +" ");
				
			}
			System.out.println();
		
		}
	}

}
 